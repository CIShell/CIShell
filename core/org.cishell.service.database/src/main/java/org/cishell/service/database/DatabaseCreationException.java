package org.cishell.service.database;

public class DatabaseCreationException extends Exception {
	
	private static final long serialVersionUID = 1L;

	//Constructors from Exception superclass
	
	public DatabaseCreationException() {
		super();
	}

	public DatabaseCreationException(String arg0) {
		super(arg0);
	}

	public DatabaseCreationException(Throwable arg0) {
		super(arg0);
	}

	public DatabaseCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
